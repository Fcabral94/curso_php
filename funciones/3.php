<?php
function suma($n1, $n2)
{
    echo $n1 + $n2;
}

function resta($n1, $n2)
{
    echo $n1 - $n2;
}

function division($n1, $n2)
{
    echo $n1 / $n2;
}

function multiplicar($n1, $n2)
{
    echo $n1 * $n2;
}
suma(2, 4);
echo '<br>';
resta(4, 1);
echo '<br>';
division(10,2);
echo '<br>';
multiplicar(2,8);
